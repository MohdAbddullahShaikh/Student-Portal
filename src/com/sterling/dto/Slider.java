package com.sterling.dto;

public class Slider {
	
	private int slideId = 0;
	private String slideHeading = null;
	private String slideContent = null;
	private String slideImageName = null;
	
	public int getSlideId() {
		return slideId;
	}
	public void setSlideId(int slideId) {
		this.slideId = slideId;
	}
	public String getSlideHeading() {
		return slideHeading;
	}
	public void setSlideHeading(String slideHeading) {
		this.slideHeading = slideHeading;
	}
	public String getSlideContent() {
		return slideContent;
	}
	public void setSlideContent(String slideContent) {
		this.slideContent = slideContent;
	}
	public String getSlideImageName() {
		return slideImageName;
	}
	public void setSlideImageName(String slideImageName) {
		this.slideImageName = slideImageName;
	}

}
